CSoundManager::CSoundManager()
{
	if( this->nインスタンス数++ == 0 )
	{
		this->pDS = NULL;
		dbMP3再生遅延時間ms = 0.0;
	}
	this->soundList = this->soundListLast = NULL;
}
CSoundManager::~CSoundManager()
{
	if( --this->nインスタンス数 == 0 )
		SAFE_RELEASE( this->pDS );
}
HRESULT CSoundManager::Init( HWND hWnd, DWORD dwCoopLevel )
{
    HRESULT	hr;

	// 利用中なら先に終了させる
	if( this->soundList )
		Term();

	// 以下、初めてのインスタンスの場合に実行。
	if( this->pDS == NULL )
	{
		// DirectSound の作成
		if( FAILED( hr = DirectSoundCreate8( NULL, &this->pDS, NULL ) ) )
			return hr;

		// 協調レベルの設定
		if( FAILED( hr = this->pDS->SetCooperativeLevel( hWnd, dwCoopLevel ) ) )
		{
			SAFE_RELEASE( this->pDS );
		    return hr;
		}

		// 遅延時間の算出
		this->tMP3遅延時間を計算し記憶する();
	}
    return S_OK;
}

void	CSoundManager::Term()
{
	SoundList *cell, *next;
	cell = this->soundList;
	while( cell != NULL )
	{
		next = cell->next;
		if( cell->pSound ) cell->pSound->Term();
		REMOVELIST( this->soundList, this->soundListLast, cell );
		SAFE_DELETE( cell->pSound );
		SAFE_DELETE( cell );
		cell = next;
	}
}
//
CSound* CSoundManager::pキャッシュされないサウンドを生成して返す( LPCTSTR filename )
{
	HRESULT hr;

	CSound* cell = new CSound();
	cell->Init();
	if( FAILED( hr = cell->CreateFromFile( this->pDS, filename ) ) )
	{
		cell->Term();
		SAFE_DELETE( cell );
		return NULL;			// 作成に失敗
	}
	return cell;
}
//
CSoundStream* CSoundManager::pキャッシュされないストリームサウンドを生成して返す( LPCTSTR filename )
{
	HRESULT hr;

	CSoundStream* cell = new CSoundStream();
	cell->Init();
	if( FAILED( hr = cell->CreateFromFile( this->pDS, filename ) ) )
	{
		cell->Term();
		SAFE_DELETE( cell );
		return NULL;			// 作成に失敗
	}

	// 内部の CSoundStream インスタンスリストに加える。
	this->setSoundStream.insert( cell );

	return cell;
}
void	CSoundManager::tキャッシュされないストリームサウンドを削除する( CSoundStream* pSoundStream )
{
	if( ! pSoundStream )
		return;

	if( pSoundStream->b再生中 )
		pSoundStream->Stop();

	// 内部の CSoundStream インスタンスリストから削除する。
	this->setSoundStream.erase( pSoundStream );
	SAFE_DELETE( pSoundStream );
}
void	CSoundManager::tストリームサウンドの再生処理()
{
	CSoundStream* pss;
	
	this->setSoundStream.resetList();
	while( ( pss = this->setSoundStream.getNext() ) != NULL )
		pss->StreamWork();
}
//
void	CSoundManager::t使用フラグをクリアし複製セルを削除する()
{
	// 複製は削除し、原版はフラグクリアのみ。
	SoundList* cell = this->soundList, *next;
	while( cell != NULL )
	{
		next = cell->next;

		if( cell->b複製である )
		{
			cell->pSound->Term();
			REMOVELIST( this->soundList, this->soundListLast, cell );
			SAFE_DELETE( cell->pSound );
			SAFE_DELETE( cell );
		}
		else
			cell->b使用する = false;
	
		cell = next;
	}
}

CSound* CSoundManager::pキャッシュ対応サウンドを作成して返す( LPCTSTR filename )
{
	// ファイルの最終更新時刻の取得；
	// fileInfo ← 指定されたファイルの最終更新時刻
	WIN32_FILE_ATTRIBUTE_DATA fileInfo;
	if( ! GetFileAttributesEx( filename, GetFileExInfoStandard, &fileInfo) )
		return NULL;		// ファイルの情報取得に失敗

	// キャッシュ内を探す
	SoundList* cell;
	for( cell = this->soundList; cell != NULL; cell = cell->next )
	{
		if( lstrcmp( filename, cell->strファイル名 ) == 0 
			&& ( fileInfo.ftLastWriteTime.dwLowDateTime  == cell->ft最終更新時刻.dwLowDateTime )
			&& ( fileInfo.ftLastWriteTime.dwHighDateTime == cell->ft最終更新時刻.dwHighDateTime ) )
		{
			// 同一のものがあった
			if( ! cell->b使用する )
			{
				// 最初の１個は原版をそのまま使う
				cell->b使用する = true;
				cell->b複製である = false;
				return cell->pSound;
			}
			else
			{
				// ２個目以降は複製を生成
				SoundList* ncell = NULL;
				ncell = new SoundList();
				ncell->b使用する = true;
				ncell->b複製である = true;
				ncell->ft最終更新時刻 = cell->ft最終更新時刻;
				lstrcpyn( ncell->strファイル名, cell->strファイル名, _MAX_PATH );
				ncell->prev = ncell->next = NULL;
				ncell->pSound = new CSound();
				ncell->pSound->Init();
				if( FAILED( ncell->pSound->DuplicateFromSound( this->pDS, cell->pSound ) ) )
				{
					ncell->pSound->Term();
					SAFE_DELETE( ncell->pSound );
					SAFE_DELETE( ncell );
					return NULL;	// キャッシュからの複製に失敗
				}
				APPENDLIST( this->soundList, this->soundListLast, ncell );
				return ncell->pSound;
			}
		}
	}

	// キャッシュになかったので新規生成する
	HRESULT hr;
	cell = new SoundList();
	cell->pSound = new CSound();
	cell->pSound->Init();
	if( FAILED( hr = cell->pSound->CreateFromFile( this->pDS, filename ) ) )
	{
		SAFE_DELETE( cell->pSound );
		SAFE_DELETE( cell );
		return NULL;		// サウンドバッファの新規作成に失敗
	}
	cell->b使用する = true;
	cell->b複製である = false;
	cell->ft最終更新時刻 = fileInfo.ftLastWriteTime;
	lstrcpyn( cell->strファイル名, filename, _MAX_PATH );
	cell->prev = cell->next = NULL;
	APPENDLIST( this->soundList, this->soundListLast, cell );

	return cell->pSound;
}

void	CSoundManager::t未使用サウンドを削除する()
{
	SoundList* cell = this->soundList, *next;
	while( cell != NULL )
	{
		next = cell->next;
		if( ! cell->b使用する )
		{
			cell->pSound->Term();
			REMOVELIST( this->soundList, this->soundListLast, cell );
			SAFE_DELETE( cell->pSound );
			SAFE_DELETE( cell );
		}
		cell = next;
	}
}

//
HRESULT CSoundManager::SetPrimaryBufferFormat( DWORD dwPrimaryChannels, DWORD dwPrimaryFreq, DWORD dwPrimaryBitRate )
{
    HRESULT hr;
    LPDIRECTSOUNDBUFFER pDSBPrimary = NULL;

	if( this->pDS == NULL )
		return FDKERR_DirectSoundがNULL;

	// プライマリバッファの取得
    DSBUFFERDESC dsbd;
    ZeroMemory( &dsbd, sizeof(DSBUFFERDESC) );
	dsbd.dwSize        = sizeof(DSBUFFERDESC);
	dsbd.dwFlags       = DSBCAPS_PRIMARYBUFFER;
	dsbd.dwBufferBytes = 0;
    dsbd.lpwfxFormat   = NULL;
	if( FAILED( hr = this->pDS->CreateSoundBuffer( &dsbd, &pDSBPrimary, NULL ) ) )
		return hr;	// プライマリバッファの取得に失敗

	// フォーマットの設定
    WAVEFORMATEX wfx;
    ZeroMemory( &wfx, sizeof(WAVEFORMATEX) ); 
	wfx.wFormatTag      = (WORD)  WAVE_FORMAT_PCM; 
    wfx.nChannels       = (WORD)  dwPrimaryChannels; 
    wfx.nSamplesPerSec  = (DWORD) dwPrimaryFreq; 
    wfx.wBitsPerSample  = (WORD)  dwPrimaryBitRate; 
	wfx.nBlockAlign     = (WORD)  (wfx.wBitsPerSample / 8 * wfx.nChannels);
    wfx.nAvgBytesPerSec = (DWORD) (wfx.nSamplesPerSec * wfx.nBlockAlign);
    if( FAILED( hr = pDSBPrimary->SetFormat(&wfx) ) )
        return hr;		// プライマリバッファのフォーマットの設定に失敗

	// プライマリバッファの解放
	SAFE_RELEASE( pDSBPrimary );
    return S_OK;
}

HRESULT CSoundManager::GetPrimaryBufferFormat( DWORD* pdwPrimaryChannels, DWORD* pdwPrimaryFreq, DWORD* pdwPrimaryBitRate )
{
    HRESULT hr;
    LPDIRECTSOUNDBUFFER pDSBPrimary = NULL;

	if( this->pDS == NULL )
		return FDKERR_DirectSoundがNULL;

	// プライマリバッファの取得
    DSBUFFERDESC dsbd;
    ZeroMemory( &dsbd, sizeof(DSBUFFERDESC) );
	dsbd.dwSize        = sizeof(DSBUFFERDESC);
	dsbd.dwFlags       = DSBCAPS_PRIMARYBUFFER;
	dsbd.dwBufferBytes = 0;
    dsbd.lpwfxFormat   = NULL;
	if( FAILED( hr = this->pDS->CreateSoundBuffer( &dsbd, &pDSBPrimary, NULL ) ) )
		return hr;	// プライマリバッファの取得に失敗

	// フォーマットの取得
    WAVEFORMATEX wfx;
    ZeroMemory( &wfx, sizeof(WAVEFORMATEX) ); 
	if( FAILED( hr = pDSBPrimary->GetFormat( &wfx, sizeof(WAVEFORMATEX), NULL ) ) )
		return hr;

	*pdwPrimaryChannels = (DWORD) wfx.nChannels;
	*pdwPrimaryFreq = (DWORD) wfx.nSamplesPerSec;
	*pdwPrimaryBitRate = (DWORD) wfx.wBitsPerSample;

	// プライマリバッファの解放
	SAFE_RELEASE( pDSBPrimary );
    return S_OK;
}
//
void	CSoundManager::tMP3遅延時間を計算し記憶する()
{
	HRESULT hr;

	// g_SampleMP3[] からサウンドバッファを作成
	CSound sd;
	sd.Init();
	if( FAILED( hr = sd.CreateFromMemory( this->pDS, g_SampleMP3, 16301 ) ) )
	{
		sd.Term();
		return;		// SampleMP3 から CSound を生成することに失敗
	}

	// WAVEFORMATEX の取得
	DWORD dwSize;
	sd.GetDirectSoundBuffer()->GetFormat( NULL, 0, &dwSize );
	WAVEFORMATEX* pWF = (LPWAVEFORMATEX) malloc( dwSize );
	sd.GetDirectSoundBuffer()->GetFormat( pWF, dwSize, NULL );

	// サウンドバッファの Lock
	LPVOID bufp1 = NULL, bufp2 = NULL;
	DWORD dwSize1 = 0, dwSize2 = 0;
	if( FAILED( hr = sd.GetDirectSoundBuffer()->Lock( 0, 0, &bufp1, &dwSize1, &bufp2, &dwSize2, DSBLOCK_ENTIREBUFFER) ) )
	{
		SAFE_FREE( pWF );
		sd.Term();
		return;		// サウンドバッファの Lock に失敗
	}

	// 解析
	BYTE *p = (LPBYTE) bufp1;
	int bn = 0;
	for( WORD i = 0; i < dwSize1; i++ )
	{
		DWORD data = 0;
		for( int j = 0; j < pWF->nBlockAlign; j++ )
		{
			data = (data << 8) | ((DWORD)(*p));
			p++;
		}
		// d1=左、d2=右。
		// まあ SampleMP3 は ch2, 16bit と分ってはいるが念のためフォーマット情報を見て分岐。
		SHORT d1, d2;
		if( pWF->nChannels == 1 )
		{
			if (pWF->wBitsPerSample == 16) {		// ch1, 16bit
				d1 = 0;
				d2 = (SHORT) (((data & 0x00FF) << 8) | ((data & 0xFF00) >> 8));
			} else {								// ch1, 8 bit
				d1 = 0;
				d2 = (SHORT) (data & 0x00FF);
			}
		}
		else
		{
			if (pWF->wBitsPerSample == 16) {		// ch2. 16bit
				d1 = (SHORT)(((data & 0x00FF0000) >> 8) | ((data & 0xFF000000) >> 24));
				d2 = (SHORT)(((data & 0x000000FF) << 8) | ((data & 0x0000FF00) >>  8));
			} else {								// ch2. 8bit
				d1 = (SHORT) ((data & 0xFF00) >> 8);
				d2 = (SHORT) (data & 0x00FF);
			}
		}
			
		// 判定
		if (d1 > 20000 || d1 < -20000 || d2 > 20000 || d2 < -20000)
			break;
		bn ++;
	}
		
	// 計算
	dbMP3再生遅延時間ms = bn * 1000.0 / pWF->nSamplesPerSec;

	sd.GetDirectSoundBuffer()->Unlock( bufp1, dwSize1, bufp2, dwSize2 );
	SAFE_FREE( pWF );
	sd.Term();
}
